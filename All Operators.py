# Taking two numbers
a = 10
b = 5

# Arithmetic Operators
print("Arithmetic Operators:")
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)
print("Floor Division:", a // b)

# Comparison Operators
print("\nComparison Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)

# Logical Operators
print("\nLogical Operators:")
print("a > 0 and b > 0:", a > 0 and b > 0)
print("a > 0 or b < 0:", a > 0 or b < 0)
print("not(a > b):", not(a > b))

# Assignment Operators
print("\nAssignment Operators:")
c = a
print("c =", c)

c += b
print("c += b:", c)

c -= b
print("c -= b:", c)

c *= b
print("c *= b:", c)

c /= b
print("c /= b:", c)

# Bitwise Operators
print("\nBitwise Operators:")
print("a & b:", a & b)
print("a | b:", a | b)
print("a ^ b:", a ^ b)
print("~a:", ~a)
print("a << 1:", a << 1)
print("a >> 1:", a >> 1)